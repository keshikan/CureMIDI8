void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{

	if(htim->Instance == TIM7)
	{
		ledIncrementState();
	}

}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{

	for(uint32_t i=0; i<MIDI_IN_JACK_NUM; i++){
		if(huart->Instance == uart_instance[i]){
			ledActivate(i);
			midiSetFromJackRx(i, &uart_rx_dat[i]);
			HAL_UART_Receive_DMA(uart_handler[i], &uart_rx_dat[i],1);
			break;
		}
	}
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart)
{
	for(uint32_t i=0; i<MIDI_OUT_JACK_NUM; i++){
		if(huart->Instance == uart_instance[i]){
			ledActivate(MIDI_IN_JACK_NUM+i);
			isUartReady[i] = true;
			break;
		}
	}
}

//call after
void sendUart(uint8_t ch, uint8_t* dat)
{
	isUartReady[ch] = false;
	  __disable_irq();
	HAL_UART_Transmit_IT(uart_handler[ch], dat,1);
	  __enable_irq();
}

